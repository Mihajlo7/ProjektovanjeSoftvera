    private void btnGetAllBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetAllBooksActionPerformed
        // TODO add your handling code here:
        //postaviPrimerke();
        HashMap<String,String> kriterijum=new HashMap<>();
        if(!txtIdBook.getText().isEmpty()){
            kriterijum.put("sifraKnjige", txtIdBook.getText());
        }
        if(!txtBookName.getText().isEmpty()){
            kriterijum.put("nazivKnjige",txtBookName.getText());
        }
        if(!txtAutor.getText().isEmpty()){
            kriterijum.put("autorKnjige", txtAutor.getText());
        }
        if(comboGenre.getSelectedIndex()>-1){
            kriterijum.put("zanrKnjige", (String) comboGenre.getSelectedItem());
        }
        comboGenre.setSelectedIndex(-1);
        pronadjiPoKriterijumu(kriterijum);
    }//GEN-LAST:event_btnGetAllBooksActionPerformed

