    private void btnPrikaziDetaljeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziDetaljeActionPerformed
        // TODO add your handling code here:
        int selected=tableBook.getSelectedRow();
        if(selected>-1){
            try{
            Knjiga knjiga=ucitajKnjigu(selected);
            FrmBookDetail forma=new FrmBookDetail(BookMod.CHANGE_MOD, knjiga);
            forma.setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(this, "Ucitana je knjiga sa sifrom:"+knjiga.getSifra());
            } catch(Exception ex){
                JOptionPane.showMessageDialog(this,"Sistem ne moze da ucita knjigu","GRESKA",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Morate izabrati jednu knjigu","Izaberite kjigu",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnPrikaziDetaljeActionPerformed

    private void btnDeleteBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBooksActionPerformed
        // TODO add your handling code here:
        int selected=tableBook.getSelectedRow();
        if(selected>-1){
            KnjigeTableModel model=(KnjigeTableModel) tableBook.getModel();
            Knjiga knjiga=model.getKnjige().get(selected);
            obrisiKnjigu(knjiga);
            ((KnjigeTableModel)tableBook.getModel()).obrisiRed(selected, knjiga);
        }else{
            JOptionPane.showMessageDialog(this, "Morate izabrati jednu knjigu","Izaberite kjigu",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteBooksActionPerformed


