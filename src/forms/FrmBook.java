    private void postaviKnjige() {
        try {
            tableBook.setFont(new Font("Serif",Font.PLAIN,15));
            KnjigeTableModel model=new KnjigeTableModel();
           // System.out.println("1");
            List<Knjiga> knjige=OpstiKontrolerKI.getInstance().ucitajKnjige();
            //System.out.println("2");

            model.setKnjige(OpstiKontrolerKI.getInstance().ucitajKnjige());
            tableBook.setModel(model);
        } catch (ServerskiException ex) {
            JOptionPane.showMessageDialog(this, ex,"GRESKA",JOptionPane.ERROR_MESSAGE);
            dispose();
        } catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Doslo je do prekida rada servera!","GRESKA",JOptionPane.ERROR_MESSAGE);
        }
    }
