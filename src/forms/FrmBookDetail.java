    private void namestiPolja() {
        namestiCombo();
        namestiErrorLbl();
        switch(mod){
            case CREATE_MOD:
                //kreirajClana();
                setTitle("Kreiranje knjige");
                comboGenre.setSelectedIndex(-1);
                comboIzdavaci.setSelectedIndex(-1);
                btnDeleteBook.setEnabled(false);
                btnIzmeni.setEnabled(false);
                btnSacuvajIzmene.setEnabled(false);
                podesiPolja(false);
                break;
            case CHANGE_MOD:
                setKnjiga();
                setTitle("Izmena podataka knjige");
                namestiPolja(false);
                btnCreateBook.setEnabled(false);
                btnSacuvajIzmene.setEnabled(false);
                btnKreirajKnjigu.setEnabled(false);
                break;
                
                
                
        }
    }

    private void namestiCombo() {
        try {
            DefaultComboBoxModel modelZ=new DefaultComboBoxModel( OpstiKontrolerKI.getInstance().ucitajZanrove().toArray());
            comboGenre.setModel(modelZ);
            DefaultComboBoxModel modelI=new DefaultComboBoxModel(OpstiKontrolerKI.getInstance().ucitajIzdavace().toArray());
            comboIzdavaci.setModel(modelI);
        } catch (ServerskiException ex) {
            JOptionPane.showMessageDialog(this, "Ne moze se ucitati lista zanrova ili izdavaca!","GRESKA",JOptionPane.ERROR_MESSAGE);
        } catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Prekid rada servera!","GRESKA",JOptionPane.ERROR_MESSAGE);
        }
    }

    private void kreirajClana() throws Exception {
        
            knjiga=OpstiKontrolerKI.getInstance().kreirajKnjigu(knjiga);
            if(knjiga!=null){
                txtIdBook.setText(String.valueOf(knjiga.getSifra()));
                JOptionPane.showMessageDialog(this,"Kreirana je knjiga sa sifrom "+knjiga.getSifra());
            }
        
    }
    private void namestiErrorLbl(){
        lblNazivKnjige.setVisible(false);
        lblGodina.setVisible(false);
        lblZanr.setVisible(false);
        lblIzdavac.setVisible(false);
        lblAutor.setVisible(false);
    }
    private boolean proveriPolja(){
        boolean uspesan=true;
        if(txtBookName.getText().isEmpty()){
            uspesan=false;
            lblNazivKnjige.setVisible(true);
        }else{
            lblNazivKnjige.setVisible(false);
        }
        
        try{
        int year=Integer.parseInt(txtYear.getText());
        } catch (NumberFormatException ex){
            uspesan=false;
            lblGodina.setVisible(true);
        }
        
        if(txtYear.getText().isEmpty()){
            uspesan=false;
            lblGodina.setVisible(true);
        }else{
            lblGodina.setVisible(false);
        }
        
        if(txtAuthorName.getText().isEmpty() || txtAuthorLastname.getText().isEmpty()){
            uspesan=false;
            lblAutor.setVisible(true);
        }else{
            lblAutor.setVisible(false);
        }
        
        if(comboGenre.getSelectedIndex()==-1){
            uspesan=false;
            lblZanr.setVisible(true);
        }else{
            lblZanr.setVisible(false);
        }
        
        if(comboIzdavaci.getSelectedIndex()==-1){
            uspesan=false;
            lblIzdavac.setVisible(true);
        }else{
            lblIzdavac.setVisible(false);
        }
        return uspesan;
    }

    private void zapamtiKnjigu() throws Exception {
        
            getKnjiga();
            boolean uspesan=OpstiKontrolerKI.getInstance().zapamtiKnjigu(knjiga);
            if(uspesan){
                JOptionPane.showMessageDialog(this, "Uspesno ste zapamtili knjigu sa sifrom"+knjiga.getSifra());
                //dispose();
            }
        
        if(mod==BookMod.CHANGE_MOD){
            new FrmBook().setVisible(true);
        }
    }

    private void getKnjiga() {
        knjiga.setNaziv(txtBookName.getText());
        knjiga.setGodinaIzdavanja(Integer.parseInt(txtYear.getText()));
        knjiga.setZanr((String) comboGenre.getSelectedItem());
        knjiga.setIzdavac((Izdavac) comboIzdavaci.getSelectedItem());
        knjiga.setAutor(txtAuthorName.getText()+" "+txtAuthorLastname.getText());
        
        
    }

    private void obrisiKnjigu() throws Exception {
        
            boolean uspesan=OpstiKontrolerKI.getInstance().obrisiKnjigu(knjiga);
            if(uspesan){
               JOptionPane.showMessageDialog(this, "Obrisana je knjiga sa sifrom: "+knjiga.getSifra());
            }
        
    }

    private void setKnjiga() {
        txtIdBook.setText(String.valueOf(knjiga.getSifra()));
        txtBookName.setText(knjiga.getNaziv());
        String[] reci=knjiga.getAutor().split(" ");
        txtAuthorName.setText(reci[0]);
        txtAuthorLastname.setText(reci[1]);
        txtYear.setText(String.valueOf(knjiga.getGodinaIzdavanja()));
        comboGenre.getModel().setSelectedItem(knjiga.getZanr());
        comboIzdavaci.getModel().setSelectedItem(knjiga.getIzdavac());
    }

    private void namestiPolja(boolean b) {
        txtAuthorName.setEnabled(b);
        txtAuthorLastname.setEnabled(b);
        txtBookName.setEnabled(b);
        txtYear.setEnabled(b);
        comboGenre.setEnabled(b);
        comboIzdavaci.setEnabled(b);
    }

    private void podesiPolja(boolean b) {
        btnKreirajKnjigu.setEnabled(!b);
        btnCreateBook.setEnabled(b);
        txtBookName.setEditable(b);
        txtAuthorName.setEditable(b);
        txtAuthorLastname.setEditable(b);
        txtYear.setEditable(b);
        comboGenre.setEditable(b);
        comboIzdavaci.setEditable(b);
        //btnDeleteBook.setEnabled(b);
        
    }

    private void refresujPolja() {
        txtIdBook.setText("");
        txtBookName.setText("");
        txtAuthorName.setText("");
        txtAuthorLastname.setText("");
        txtYear.setText("");
        comboGenre.setSelectedIndex(-1);
        comboIzdavaci.setSelectedIndex(-1);
    }
