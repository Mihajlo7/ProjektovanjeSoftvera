    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        if(knjiga==null && mod==BookMod.CREATE_MOD){
            dispose();
        }
        if(knjiga!=null && mod==BookMod.CREATE_MOD){
            int option=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da hocete da izadjete?\nUkoliko izadjete podaci nece biti sacuvani!","",JOptionPane.YES_NO_OPTION);
            if(option==0){
                try {
                    obrisiKnjigu();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da obrise knjigu","GRESKA",JOptionPane.ERROR_MESSAGE);
                }
                dispose();
            }
        }
        if(mod==BookMod.CHANGE_MOD){
            dispose();
            
        }
        //new FrmBook().setVisible(true);
    }//GEN-LAST:event_btnBackActionPerformed

