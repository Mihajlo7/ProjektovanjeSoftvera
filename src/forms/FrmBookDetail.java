    private void btnCreateBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateBookActionPerformed
        // TODO add your handling code here:
        if(proveriPolja()){
            try {
                JOptionPane.showMessageDialog(this, "Uspesno ste popunili polja!");
                zapamtiKnjigu();
                int option=JOptionPane.showConfirmDialog(this, "Da li hocete da kreirate jos knjiga?","Kreiranje clana",JOptionPane.YES_NO_OPTION);
                if(option==0){
                    refresujPolja();
                    kreirajClana();
                    podesiPolja(true);
                }else{
                    dispose();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti knjigu","GRESKA",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nepravilno uneta polja!!","GRESKA",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCreateBookActionPerformed

