    private void uspostaviKonekciju() throws IOException {
        OpstiKontrolerClanaKI.getInstance().uspostaviKonekciju();
    }

    

    private void upozorenja() {
        lblUpozorenjeEmail.setVisible(false);
        lblUpozorenjeLozinka.setVisible(false);
    }

    private boolean proveriEmail() {
        if(txtEmail.getText().equals("")){
            lblUpozorenjeEmail.setVisible(true);
            return false;
            }
        return true;
    }

    private boolean proveriLozinku(){
        if(txtLozinka.getText().equals("")){
            lblUpozorenjeLozinka.setVisible(true);
            return false;
        }
        return true;
    }

    private void raskiniKonekciju() throws Exception {
        OpstiKontrolerClanaKI.getInstance().raskiniKonekciju();
    }

    private void ulogujClana(String email, String lozinka) throws ServerskiException,Exception {
       clan=new Clan();
       clan=OpstiKontrolerClanaKI.getInstance().prijaviClana(email, lozinka);
       if(clan==null){
           JOptionPane.showMessageDialog(this, "Neuspesna prijava na sistem","Prijava",JOptionPane.WARNING_MESSAGE);
       }else{
           JOptionPane.showMessageDialog(this, "Uspesna prijava na sistem");
           dispose();
           new FrmBookView(clan).setVisible(true);
       }
    }

    
