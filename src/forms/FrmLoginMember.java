    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            uspostaviKonekciju();
            String email=txtEmail.getText();
            String lozinka=txtLozinka.getText();
            if(proveriEmail() && proveriLozinku()){
                upozorenja();
                ulogujClana(email,lozinka);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Ne mozete se povezati na server!\nPokusajte kasnije","GRESKA",JOptionPane.ERROR_MESSAGE);
        } catch(ServerskiException ex){
            JOptionPane.showMessageDialog(this, ex,"GRESKA",JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Doslo je do greske prilikom pretrage Clana!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

