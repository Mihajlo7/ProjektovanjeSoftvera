    private void btnPretraziClanoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziClanoveActionPerformed
        // TODO add your handling code here:
        HashMap<String,String> kriterijum=new HashMap<>();
        if(!txtTelefon.getText().isEmpty()){
            kriterijum.put("telefonClana",txtTelefon.getText());
        }
        try {
            modelClanovi.setClanovi(OpstiKontrolerKI.getInstance().nadjiClanove(kriterijum));
            tableClanovi.setModel(modelClanovi);
        } catch (ServerskiException ex) {
            JOptionPane.showMessageDialog(this, ex,"GRESKA",JOptionPane.ERROR_MESSAGE);
        } catch( Exception ex){
            JOptionPane.showMessageDialog(this,"Doslo je do prekida rada servera!","GRESKA",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnPretraziClanoveActionPerformed

    private void btnProveriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProveriActionPerformed
        // TODO add your handling code here:
        int selected=tableClanovi.getSelectedRow();
        if(selected==-1){
            JOptionPane.showMessageDialog(this, "Morate odabrati jednog clana!","Upozorenje",JOptionPane.WARNING_MESSAGE);
        }else{
            try {
                setRezervacija(selected); 
                ucitajRezervacije();
            } catch (ServerskiException ex) {
                JOptionPane.showMessageDialog(this, ex,"GRESKA",JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex){
                JOptionPane.showMessageDialog(this, "Doslo je do prekida rada servera!","GRESKA",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnProveriActionPerformed

    private void btnPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotvrdiActionPerformed
        try {
            // TODO add your handling code here:
            boolean uspesno=potvrdiRezervaciju();
            if(uspesno){
                JOptionPane.showMessageDialog(this,"Uspesno ste izvrsili potvrdu rezervacije");
            }else{
                JOptionPane.showMessageDialog(this, "Neuspesna opcija potvrde rezervacije","Neuspesna potvrda",JOptionPane.WARNING_MESSAGE);
            }
            dispose();
        } catch (ServerskiException ex) {
            JOptionPane.showMessageDialog(this, ex,"GRESKA",JOptionPane.ERROR_MESSAGE);
        } catch( Exception ex){
            JOptionPane.showMessageDialog(this,"Doslo je do prekida rada servera!","GRESKA",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPotvrdiActionPerformed

