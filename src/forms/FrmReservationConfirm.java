    private void namestiPolja() {
        try {
            namestiTabeleKnjige();
            namestiTabeluClanovi();
            tableStavke.setEnabled(false);
            btnPotvrdi.setEnabled(false);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ne moze se ucitati lista objekata!","GRESKA",JOptionPane.ERROR_MESSAGE);
        }

    }

    private void namestiTabeleKnjige() {
       modelStavke=new KnjigeTableModel();
       tableStavke.setFont(new Font("Serif",Font.PLAIN,14));
       tableStavke.setModel(modelStavke);
    }

    private void namestiTabeluClanovi() throws Exception {
       modelClanovi=new ClanoviTableModel();
            tableClanovi.setFont(new Font("Serif",Font.PLAIN,14));
            //ClanoviTableModel model=new ClanoviTableModel();
            modelClanovi.setClanovi(OpstiKontrolerKI.getInstance().ucitajSveClanove());
            setTitle("Potvrdi rezervaciju");
            tableClanovi.setModel(modelClanovi); 
    }

    private void ucitajRezervacije() throws ServerskiException,Exception {
        List<Rezervacija> rezervacije=OpstiKontrolerKI.getInstance().ucitajRezervacijePoClanu(rezervacija);
        if(rezervacije.size()>0){
          FrmSelectReservation forma=new FrmSelectReservation(this,true);
          forma.setRezervacije(rezervacije);
          forma.setForma(this);
          forma.namestiPolja();
          forma.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Nema rezervacija za datog clana!","Rezervacije",JOptionPane.WARNING_MESSAGE);
        }
    }

    private void setRezervacija(int selected) {
        rezervacija=new Rezervacija();
        Clan clan=((ClanoviTableModel)tableClanovi.getModel()).getClanovi().get(selected);
        rezervacija.setClan(clan);
    }

    void namestiRezervaciju(Rezervacija rezervacija) {
        this.rezervacija=rezervacija;
    }

    void getRezervacija() {
        txtSifraRezervacije.setText(String.valueOf(rezervacija.getSifra()));
        txtDatumIznamljivanja.setText(rezervacija.getDatumIznamljivanja().toString());
        txtDatumVracanja.setText(rezervacija.getDatumVracanja().toString());
        StavkeRezervacijeModel model=new StavkeRezervacijeModel();
        model.setStavke_rezervacije(rezervacija.getStavkeRezervacije());
        tableStavke.setModel(model);
        if(rezervacija.isAktivnostRezervacije()){
            btnPotvrdi.setEnabled(true);
        }
    }

    private boolean potvrdiRezervaciju() throws Exception {
        return OpstiKontrolerKI.getInstance().potvrdiRezervaciju(rezervacija);
    }
